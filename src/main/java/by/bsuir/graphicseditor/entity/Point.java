package by.bsuir.graphicseditor.entity;

public class Point {
    private double x;
    private double y;
    private double z;
    private double w;

    public Point() {
        x = 0;
        y = 0;
        z = 0;
        w = 0;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }

    public double getW() {
        return w;
    }

    public void setW(double w) {
        this.w = w;
    }
}
